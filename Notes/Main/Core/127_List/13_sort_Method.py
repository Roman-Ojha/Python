# sort()
# This method is used to sort the element of the list into ascending order.
# Syntax: list_name.sort()

# Sort Int type
a = [30, 20, 22, 11, 2]
print("Before Sort: ", a)
# Before Sort:  [30, 20, 22, 11, 2]

a.sort()
print("After Sort: ", a)
# After Sort:  [2, 11, 20, 22, 30]


# Sort String type:
a = ["Roman", "Jack"]
print("Before Sort: ", a)
a.sort()
print("After Sort: ", a)
