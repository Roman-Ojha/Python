# count():
# This method returns number of occurrence of the specified element in the list.
# Syntax: list_name.count(<element>)

a = [10, 20, 30, "Roman", 30, 10, 30]
print(a)
# [10, 20, 30, "Roman", 30, 10, 30]

num = a.count(10)
print("10 Element came ", num, " No. of times")
# 10 Element came  2  No. of times
